#include "drivers\stm32f3_system.h"
#include "modules\module_serial.h"

#include "main.h"
/*====================================================================================================*/
/*====================================================================================================*/
void System_Init( void )
{
  HAL_Init();
  GPIO_Config();
  Serial_Config();

  delay_ms(100);
  printf("\fHello World!\r\n\r\n");
}

int main( void )
{
  System_Init();

  while(1) {
    LED_Toggle();
    delay_ms(500);
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void Serial_evenCallBack( void )
{
  uint8_t recvData = 0;

  LED_Toggle();
  recvData = Serial_RecvByte();
  if(recvData == 0x0D)
    Serial_SendStr("\r\n");
  else
    Serial_SendByte(recvData);
}
