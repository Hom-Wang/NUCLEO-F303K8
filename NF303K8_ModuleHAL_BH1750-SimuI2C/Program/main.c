#include "drivers\stm32f3_system.h"
#include "modules\module_serial.h"
#include "modules\module_bh1750.h"

#include "main.h"
/*====================================================================================================*/
/*====================================================================================================*/
void System_Init( void )
{
  HAL_Init();
  GPIO_Config();
  Serial_Config();
  BH1750_Config();

  delay_ms(100);
  printf("\fHello World!\r\n\r\n");

  BH1750_Init();
}

int main( void )
{
  System_Init();

  while(1) {
    LED_Toggle();
    delay_ms(25); // wait conv
    printf("%d\r", BH1750_getData());
  }
}
